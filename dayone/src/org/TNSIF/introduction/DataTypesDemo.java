//program to demonstrate on various Datatype
package org.TNSIF.introduction;

public class DataTypesDemo {

	public static void main(String[] args) {
		//integer will give us the values in whole number
		//float and double will gives us the value 
		int num1=5/2;
		float num2=102/45;
		/* if we use d after any number for double type it will give 
		 * *the precision upto 16 decimal places
		 */
		double num3=10d/3d;
		float num4=14f/3f;
		System.out.print("the value1 is:"+num1);
		System.out.print("the value2 is:"+num2);
		System.out.print("the value3 is:"+num3);
		System.out.print("the value4 is:"+num4);
	}

}

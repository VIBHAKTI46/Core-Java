//Program to demonstrate  on autoxing
/* autoxing: conversion of primitive to object type
 * example char to character
 */
package org.tnsif.autoandunboxing;

public class AutoboxingExample {

	public static void main(String[] args) {
		//primitive variable
	int num=1045;
	//object
	//converting primitive to object type
	Integer x=num;
	System.out.print("value of x is :"+x);
	}

}
